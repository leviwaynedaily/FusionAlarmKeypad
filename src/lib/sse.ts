// Server-Sent Events (SSE) client for real-time event streaming - MEMORY OPTIMIZED VERSION
import { logger } from './logger';

// üî• FIX: Safe browser-compatible development check
const isProduction = typeof window !== 'undefined' && window.location.hostname !== 'localhost';
const isDev = !isProduction;

// üî• FIX: Expanded categories to capture all device events
const MONITORED_CATEGORIES = [
  'alarm',
  'space', 
  'security',
  'analytics',
  'device',
  'door',
  'motion',
  'light',
  'camera'
];

export interface SSEEvent {
  type: 'connection' | 'event' | 'heartbeat' | 'system';
  organizationId?: string;
  timestamp?: string;
  data?: any;
}

export interface FusionSSEConfig {
  apiKey: string;
  baseUrl?: string;
  categories?: string[];
  eventTypes?: string[];
}

export interface FusionSSEClient {
  connect(): Promise<void>;
  disconnect(): void;
  on(event: string, callback: (...args: any[]) => void): void;
  off(event: string, callback: (...args: any[]) => void): void;
  isConnected(): boolean;
}

class SSEClient implements FusionSSEClient {
  private config: Required<FusionSSEConfig>;
  private eventListeners: Map<string, ((...args: any[]) => void)[]> = new Map();
  private connected: boolean = false;
  private lastEventTime: number = 0;
  private reconnectAttempts: number = 0;
  private maxReconnectAttempts: number = 2; // üî• FIX: Reduced from 3
  private reconnectTimer: NodeJS.Timeout | null = null;
  private abortController: AbortController | null = null;
  private isReconnecting: boolean = false;
  // üî• FIX: Add event rate limiting
  private eventRateLimit: number = 0;
  private lastEventTimestamp: number = 0;

  constructor(config: FusionSSEConfig) {
    this.config = {
      baseUrl: process.env.NEXT_PUBLIC_FUSION_BASE_URL || 'https://app.getfusion.io',
      categories: MONITORED_CATEGORIES,
      eventTypes: [],
      ...config
    };
  }

  // üîç DEBUG: Enhanced connection with detailed logging
  connect(): Promise<void> {
    return new Promise(async (resolve, reject) => {
      try {
        // Prevent multiple simultaneous connections
        if (this.connected || this.isReconnecting) {
          console.log('üîç SSE: Connection already active, skipping');
          resolve();
          return;
        }

        console.log('üîç SSE: Starting connection attempt...');
        this.disconnect(); // Clean up any existing connection

        const url = new URL('/api/events/stream', this.config.baseUrl);
        
        // ‚úÖ FIXED: Enable thumbnails to get camera images
        url.searchParams.append('includeThumbnails', 'true');

        const headers = {
          'Accept': 'text/event-stream',
          'x-api-key': this.config.apiKey
        };

        console.log('üîç SSE: Connecting to:', url.toString());
        // üîí SECURITY: Use consistent API key masking
        const maskedKey = this.config.apiKey ? `${this.config.apiKey.substring(0, 8)}...${this.config.apiKey.substring(this.config.apiKey.length - 4)}` : 'MISSING';
        console.log('üîç SSE: Headers:', { 
          'Accept': headers.Accept,
          'x-api-key': maskedKey
        });

        this.abortController = new AbortController();
        this.isReconnecting = true;

        console.log('üîç SSE: Making fetch request...');
        const response = await fetch(url.toString(), {
          method: 'GET',
          headers: headers,
          signal: this.abortController.signal,
          cache: 'no-store',
          credentials: 'omit'
        });

        console.log('üîç SSE: Response received:', {
          status: response.status,
          statusText: response.statusText,
          headers: Object.fromEntries(response.headers.entries())
        });

        if (!response.ok) {
          const errorText = await response.text();
          console.error('üîç SSE: Response not OK:', {
            status: response.status,
            statusText: response.statusText,
            body: errorText
          });
          throw new Error(`HTTP ${response.status}: ${response.statusText} - ${errorText}`);
        }

        // Verify it's actually an SSE stream
        const contentType = response.headers.get('content-type');
        if (!contentType?.includes('text/event-stream')) {
          console.error('üîç SSE: Invalid content type:', contentType);
          throw new Error(`Expected text/event-stream, got: ${contentType}`);
        }

        // ‚úÖ FIXED: Set connected state immediately after successful response
        this.connected = true;
        this.isReconnecting = false;
        this.reconnectAttempts = 0;
        this.lastEventTime = Date.now();

        console.log('üîç SSE: Connection established, emitting connected event');
        this.emit('connected');
        resolve();

        // Start processing the stream
        console.log('üîç SSE: Starting stream processing...');
        await this.processStreamingResponse(response);

      } catch (error) {
        this.isReconnecting = false;
        this.connected = false;
        
        console.error('üîç SSE: Connection error details:', {
          error: error instanceof Error ? error.message : error,
          name: error instanceof Error ? error.name : 'Unknown',
          stack: error instanceof Error ? error.stack : 'No stack',
          type: typeof error
        });
        
        if (error instanceof Error && error.name === 'AbortError') {
          console.log('üîç SSE: Connection aborted (normal for cleanup)');
          resolve();
          return;
        }

        this.emit('error', error);
        
        // Only schedule reconnect if it's not a permanent failure
        if (!(error instanceof TypeError && error.message.includes('Load failed'))) {
          this.scheduleReconnect();
        } else {
          console.log('üîç SSE: Skipping reconnect due to load failure');
        }
        
        reject(error);
      }
    });
  }

  // üî• FIX: Memory-optimized stream processing
  private async processStreamingResponse(response: Response) {
    if (!response.body) {
      throw new Error('Response body is null');
    }

    console.log('üîç SSE: Setting up stream reader...');
    const reader = response.body.getReader();
    const decoder = new TextDecoder();
    let buffer = '';
    let currentEvent: { event?: string; data?: string; id?: string } = {};

    try {
      console.log('üîç SSE: Starting stream read loop...');
      while (this.connected && !this.abortController?.signal.aborted) {
        const { done, value } = await reader.read();
        console.log('üîç SSE: Read chunk:', { done, valueLength: value?.length });
        
        if (done) {
          this.connected = false;
          this.emit('disconnected');
          this.scheduleReconnect();
          break;
        }

        const chunk = decoder.decode(value, { stream: true });
        
        // üî• FIX: Improved buffer management for large chunks
        buffer += chunk;
        if (buffer.length > 50000) { // Increased from 10KB to 50KB
          console.log('üîç SSE: Large buffer detected, trimming...', { bufferLength: buffer.length });
          buffer = buffer.slice(-25000); // Keep last 25KB instead of 5KB
        }

        // üîç DEBUG: Log chunk info for large chunks
        if (isDev && chunk.length > 1000) {
          console.log('üîç SSE: Processing large chunk:', { 
            chunkLength: chunk.length, 
            bufferLength: buffer.length,
            linesInChunk: chunk.split('\n').length,
            firstLines: chunk.split('\n').slice(0, 5),
            lastLines: chunk.split('\n').slice(-5),
            containsEvent: chunk.includes('event:'),
            containsData: chunk.includes('data:')
          });
        }

        // Process complete SSE messages
        const lines = buffer.split('\n');
        buffer = lines.pop() || '';

        // üî• FIX: Process each line with better debugging
        let eventsInChunk = 0;
        let eventLines = 0;
        let dataLines = 0;
        
        for (const line of lines) {
          const trimmedLine = line.trim();
          
          if (trimmedLine === '') {
            if (currentEvent.event || currentEvent.data) {
              eventsInChunk++;
              console.log('üîç SSE: Completing event:', {
                eventType: currentEvent.event,
                dataLength: currentEvent.data?.length,
                hasData: !!currentEvent.data
              });
              this.handleSSEEvent(currentEvent);
              currentEvent = {};
            }
          } else if (trimmedLine.startsWith('event:')) {
            currentEvent.event = trimmedLine.substring(6).trim();
            eventLines++;
          } else if (trimmedLine.startsWith('data:')) {
            const data = trimmedLine.substring(5).trim();
            dataLines++;
            // üî• FIX: Increased data size limit from 20KB to 100KB for camera thumbnails
            if (data.length < 100000) {
              currentEvent.data = (currentEvent.data || '') + data;
            } else {
              console.log('üîç SSE: Data too large, truncating:', data.length);
              // üî• FIX: Still try to process truncated data rather than skipping completely
              currentEvent.data = (currentEvent.data || '') + data.substring(0, 50000);
            }
          } else if (trimmedLine.startsWith('id:')) {
            currentEvent.id = trimmedLine.substring(3).trim();
          }
        }
        
        // üîç DEBUG: Log events processed in large chunks
        if (isDev && (eventsInChunk > 0 || eventLines > 0 || dataLines > 0)) {
          console.log('üîç SSE: Processed chunk summary:', {
            eventsCompleted: eventsInChunk,
            eventLines: eventLines,
            dataLines: dataLines,
            totalLines: lines.length,
            bufferRemaining: buffer.length
          });
        }
      }
    } catch (error) {
      if (this.abortController?.signal.aborted) {
        return;
      }
      
      this.connected = false;
      this.emit('error', error);
      this.scheduleReconnect();
    } finally {
      try {
        reader.releaseLock();
      } catch (e) {
        // Ignore cleanup errors
      }
    }
  }

  // üî• FIX: Enhanced event handling for all device types
  private handleSSEEvent(event: { event?: string; data?: string; id?: string }) {
    try {
      const now = Date.now();
      
      // üî• FIX: Reduced rate limiting - allow more events through
      if (now - this.lastEventTimestamp < 50) { // Changed from 100ms to 50ms
        this.eventRateLimit++;
        if (this.eventRateLimit > 25) { // Changed from 10 to 25
          console.log('üîç SSE: Rate limited event dropped', { eventType: event.event, rateLimit: this.eventRateLimit });
          return; // Drop events if too frequent
        }
      } else {
        this.eventRateLimit = 0;
        this.lastEventTimestamp = now;
      }

      this.lastEventTime = now;
      
      // üîç DEBUG: Log all incoming events (even empty ones)
      console.log('üîç SSE: Raw event received:', {
        event: event.event,
        hasData: !!event.data,
        dataLength: event.data?.length || 0,
        id: event.id,
        rateLimit: this.eventRateLimit
      });
      
      if (!event.data) {
        console.log('üîç SSE: Skipping event with no data');
        return;
      }

      // üî• FIX: Add debug for raw data before parsing
      if (isDev && event.data.length > 100) {
        console.log('üîç SSE: Large data received:', event.data.substring(0, 200) + '...');
      }

      let data: any;
      try {
        data = JSON.parse(event.data);
      } catch (parseError) {
        console.log('üîç SSE: JSON parse error:', parseError);
        console.log('üîç SSE: Raw data:', event.data.substring(0, 500));
        return;
      }

      // üîç DEBUG: Log all parsed events
      console.log('üîç SSE: Parsed event data:', {
        type: data.type,
        event: event.event,
        deviceName: data.deviceName,
        category: data.category,
        eventCategory: data.eventCategory,
        eventType: data.eventType,
        displayState: data.displayState,
        thumbnailData: !!data.thumbnailData,
        fullData: isDev ? data : undefined
      });

      // ‚úÖ FIXED: Handle connection events properly (don't skip them)
      if (data.type === 'connection') {
        console.log('üîç SSE: Connection event received - connection confirmed');
        this.emit('connection_confirmed', data);
        return;
      }

      // Process heartbeat events normally (don't skip - let timeline management handle filtering)
      if (data.type === 'heartbeat') {
        console.log('üîç SSE: Heartbeat event received - processing normally');
        this.emit('heartbeat', data);
        // Continue processing as a normal event
      }

      // ‚úÖ FIXED: Process ALL device events, including those with thumbnails
      const eventType = event.event || 'event';
      
      console.log('üîç SSE: Processing device event type:', eventType);
      
      // Emit based on event type from SSE stream
      if (eventType === 'event') {
        console.log('üîç SSE: Emitting security_event');
        this.emit('security_event', data);
      } else if (eventType === 'arming') {
        console.log('üîç SSE: Emitting space_state_change');
        this.emit('space_state_change', data);
      } else if (eventType === 'system') {
        console.log('üîç SSE: Emitting system_event');
        this.emit('system_event', data);
      } else if (eventType === 'error') {
        console.log('üîç SSE: Emitting error_event');
        this.emit('error_event', data);
      } else {
        console.log('üîç SSE: Emitting unknown_event for type:', eventType);
        this.emit('unknown_event', data);
      }

    } catch (error) {
      console.error('üîç SSE: Event handling error:', error);
    }
  }

  // üî• FIX: Immediate disconnect
  disconnect(): void {    
    if (this.reconnectTimer) {
      clearTimeout(this.reconnectTimer);
      this.reconnectTimer = null;
    }

    if (this.abortController) {
      this.abortController.abort();
      this.abortController = null;
    }

    if (this.connected) {
      this.connected = false;
      this.isReconnecting = false;
      this.emit('disconnected');
    }

    // üî• FIX: Clear all event listeners to prevent memory leaks
    this.eventListeners.clear();
  }

  // üî• FIX: Limited reconnection attempts
  private scheduleReconnect(): void {
    if (this.reconnectAttempts >= this.maxReconnectAttempts) {
      this.emit('error', new Error('Max retry attempts reached'));
      return;
    }

    this.reconnectAttempts++;
    const delay = Math.min(2000 * this.reconnectAttempts, 5000); // üî• FIX: Shorter delays
    
    this.reconnectTimer = setTimeout(async () => {
      try {
        await this.connect();
      } catch (error) {
        // Silent retry
      }
    }, delay);
  }

  // Event emitter methods
  on(event: string, callback: (...args: any[]) => void): void {
    if (!this.eventListeners.has(event)) {
      this.eventListeners.set(event, []);
    }
    const listeners = this.eventListeners.get(event)!;
    // üî• FIX: Increased listener limit from 5 to 20 to handle React re-mounting
    if (listeners.length < 20) {
      listeners.push(callback);
      console.log(`üîç SSE: ‚úÖ Registered listener for '${event}', total listeners: ${listeners.length}`);
      console.log(`üîç SSE: All registered events:`, Array.from(this.eventListeners.keys()));
    } else {
      console.log(`üîç SSE: ‚ùå Listener limit reached for '${event}', skipping registration`);
    }
  }

  off(event: string, callback: (...args: any[]) => void): void {
    const listeners = this.eventListeners.get(event);
    if (listeners) {
      const index = listeners.indexOf(callback);
      if (index > -1) {
        listeners.splice(index, 1);
      }
    }
  }

  private emit(event: string, data?: any): void {
    const listeners = this.eventListeners.get(event);
    console.log(`üîç SSE: Emitting '${event}' to ${listeners?.length || 0} listeners`);
    
    if (listeners && listeners.length > 0) {
      // üî• FIX: Process listeners synchronously to prevent buildup
      listeners.forEach((callback, index) => {
        try {
          console.log(`üîç SSE: Calling listener ${index + 1}/${listeners.length} for '${event}'`);
          callback(data);
          console.log(`üîç SSE: ‚úÖ Listener ${index + 1} completed successfully for '${event}'`);
        } catch (error) {
          console.log(`üîç SSE: ‚ùå Error in listener ${index + 1} for '${event}':`, error);
        }
      });
    } else {
      console.log(`üîç SSE: ‚ùå No listeners found for '${event}' - this is the problem!`);
      console.log(`üîç SSE: Available events with listeners:`, Array.from(this.eventListeners.keys()));
    }
  }

  isConnected(): boolean {
    return this.connected;
  }
}

// üî• FIX: Singleton with proper cleanup
let globalSSEClient: SSEClient | null = null;
let globalSSEConfig: string | null = null; // Track config to detect changes

export function createSSEClient(apiKey: string, options?: Partial<FusionSSEConfig>): FusionSSEClient {
  // Create a config signature to detect if we can reuse the existing client
  const configSignature = JSON.stringify({ apiKey, ...options });
  
  // üî• FIX: Only create new client if config changed or no client exists
  if (globalSSEClient && globalSSEConfig === configSignature) {
    console.log('üîç SSE: Reusing existing client with same configuration');
    return globalSSEClient;
  }
  
  // üî• FIX: Clean up existing client only if config changed
  if (globalSSEClient) {
    console.log('üîç SSE: Configuration changed, cleaning up existing client');
    globalSSEClient.disconnect();
    globalSSEClient = null;
  }
  
  console.log('üîç SSE: Creating new client with configuration:', { 
    apiKey: apiKey ? `${apiKey.substring(0, 10)}...` : 'MISSING',
    hasOptions: !!options 
  });
  
  globalSSEClient = new SSEClient({
    apiKey,
    ...options
  });
  
  globalSSEConfig = configSignature;
  
  return globalSSEClient;
} 